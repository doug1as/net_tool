def prRed(word): 
    return "\033[91m" + word + "\033[00m"

def prGreen(word): 
    return "\033[92m" + word + "\033[00m"

def prYellow(word): 
    return "\033[93m" + word + "\033[00m"

def prLightPurple(word): 
    return "\033[94m" + word + "\033[00m" 

def prPurple(word): 
    return "\033[95m" + word + "033[00m"

def prCyan(word): 
    return "\033[96m" + word + "\033[00m"

def prLightGray(word): 
    return "\033[97m" + word + "\033[00m"

def prBlack(word): 
    return "\033[98m" + word + "\033[00m"